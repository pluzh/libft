#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*c;
	size_t	i;

	c = (char *)s;
	i = 0;
	while (n > 0)
	{
		c[i] = '\0';
		n--;
		i++;
	}
}
