#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (*s == (char)c)
		return ((char *)s);
	while (*s != (char)c && *s++)
	{
		if (*s == (char)c)
			return ((char *)s);
	}
	return (NULL);
}
